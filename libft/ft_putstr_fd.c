#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		if (i == INT_MAX)
		{
			write(fd, s, INT_MAX);
			s = &s[INT_MAX];
			i = 0;
		}
		else
			i++;
	}
	write(fd, s, i);
}
